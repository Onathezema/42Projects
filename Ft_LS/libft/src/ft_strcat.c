#include "../libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int len;

	len = ft_strlen(s1);
	if (*s2 == '\0')
		return (s1);
	while (*s2)
	{
		s1[len] = *s2++;
		len++;
	}
	s1[len] = '\0';
	return (s1);
}
